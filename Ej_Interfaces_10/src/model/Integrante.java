package model;

public class Integrante {
	
	private Integer numParticipante;
	private String nombreP;
	private Integer edad;
	private String localidadP;
	
	public Integrante() {
		
	}

	public Integer getNumParticipante() {
		return numParticipante;
	}

	public void setNumParticipante(Integer numParticipante) {
		this.numParticipante = numParticipante;
	}

	public String getNombreP() {
		return nombreP;
	}

	public void setNombreP(String nombreP) {
		this.nombreP = nombreP;
	}

	public Integer getEdad() {
		return edad;
	}

	public void setEdad(Integer edad) {
		this.edad = edad;
	}

	public String getLocalidadP() {
		return localidadP;
	}

	public void setLocalidadP(String localidadP) {
		this.localidadP = localidadP;
	}
	
}
